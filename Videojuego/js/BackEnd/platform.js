//Definition of modules
const express = require('express');
const cors = require('cors');
const mysql = require('mysql2');

const app = express();
const port = 3000;

app.use(cors());

const pool = mysql.createPool({
    host: '127.0.0.1',
    user: 'root',
    password: 'Febrero_312',
    database: 'hyperjump'
}).promise();

//Generation zones backend
app.get('/zonas/:level', async (req, res) => {
    try{
    const level = req.params.level;
    
    const [zones] = await pool.query('SELECT * FROM ZonaGeneracion WHERE id_nivel = ?', [level]);

    res.json(zones);
    }catch(error){
        console.error('Error fetching generation zones:', error);
        res.status(500).json({ error: 'Error fetching generation zones' });
    }
});

//Platforms autogenerated backend
app.get('/plats/:auto', async(req, res) => {
    try{
    const auto = req.params.auto == "true" ? 1 : 0; //To detect if we want autogenerated or not platforms

    const [plats] = await pool.query('SELECT * FROM Plataforma WHERE es_autogenerada = ?', [auto]);

    res.json(plats);
    }catch(error){
        console.error('Error fetching platforms:', error);
        res.status(500).json({ error: 'Error fetching platforms' });
    }
});

app.get('/cartas/:nivel', async (req, res) => {
    try{
    const nivel = req.params.nivel;

    const [cartas] = await pool.query('SELECT * FROM Carta WHERE nivel_maximo = ?', [nivel]);

    res.json(cartas);
    }catch(error){
        console.error('Error fetching cards:', error);
        res.status(500).json({ error: 'Error fetching cards' });
    }
});

app.listen(port, () => {
    console.log(`Servidor en http://localhost:${port}`);
});