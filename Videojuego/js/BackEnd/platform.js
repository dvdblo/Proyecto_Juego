//Definition of modules
const express = require('express');
const cors = require('cors');
const mysql = require('mysql2');

const app = express();
const port = 3000;

//Actual host and DB
const host = '127.0.0.1';
const user = 'root';
const pass = 'Febrero_312';
const db = 'hyperjump';

app.use(cors());

//Generation zones backend
app.get('/zonas/:level', (req, res) => {
    const level = req.params.level;
    
    //Creates the conection
    const connection = mysql.createConnection({
        host: host,
        user: user,
        password: pass,
        database: db
    });

    //Runs the connection
    connection.connect((err) => {
        if (err) throw err;

        //To consult the generation zones
        connection.query(
            'SELECT * FROM ZonaGeneracion WHERE id_nivel = ?', [level], (err, results) => {
                if (err) throw err;

                connection.end();

                res.json(results);
            }
        );
    });
});

//Platforms autogenerated backend
app.get('/plats/:auto', (req, res) => {

    const auto = req.params.auto == "true" ? 1 : 0; //To detect if we want autogenerated or not platforms

    const connection = mysql.createConnection({
        host: host,
        user: user,
        password: pass,
        database: db
    });

    connection.connect((err) => {
        if (err) throw err;

        connection.query('SELECT * FROM Plataforma WHERE es_autogenerada = ?', [auto], (err, results) => {
                if (err) throw err;

                connection.end();
                
                res.json(results);
            }
        );
    });
});

app.get('/cartas/:nivel', (req, res) => {

    const nivel = req.params.nivel;

    const connection = mysql.createConnection({
        host: host,
        user: user,
        password: pass,
        database: db
    });

    connection.connect((err) => {
        if (err) throw err;

        connection.query('SELECT * FROM Carta WHERE nivel_maximo = ?', [nivel], (err, results) => {
                if (err) throw err;

                connection.end();
                
                res.json(results);
            }
        );
    });
});

app.listen(port, () => {
    console.log(`Servidor en http://localhost:${port}`);
});